package apps.my.p2017;

public class Food {

    String id;

    String nombre;
    String marca;
    String categoria;
    String genero;
    String descripcion;

    Float cantidad;
    Float calorias;

    Float grasas;
    Float proteinas;
    Float carbo;
    Float fibra;

    Integer custom;

    String mUserId;
    String unitId;

    Unit unit;

    public String getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getMarca() {
        return marca;
    }

    public String getCategoria() {
        return categoria;
    }

    public String getGenero() {
        return genero;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public Float getCantidad() {
        return cantidad;
    }

    public Float getCalorias() {
        return calorias;
    }

    public Float getGrasas() {
        return grasas;
    }

    public Float getProteinas() {
        return proteinas;
    }

    public Float getCarbo() {
        return carbo;
    }

    public Float getFibra() {
        return fibra;
    }

    public Integer getCustom() {
        return custom;
    }

    public String getmUserId() {
        return mUserId;
    }

    public String getUnitId() {
        return unitId;
    }

    public Unit getUnit() {
        return unit;
    }
}
